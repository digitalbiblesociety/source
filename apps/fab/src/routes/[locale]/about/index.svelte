			<h4>The Project</h4>
			<p>
				Find-A-Bible (FAB) is a web directory providing links to Bibles and
				biblical resources in the 7000+ languages of the world. The FAB
				directory primarily consists of content created by members of the Forum
				of Bible Agencies International (FOBAI) and is intended to help users
				discover and obtain Scriptures and biblical resources in the language
				and media of their choice for discipleship, evangelism, and church
				planting. Find-A-Bible also features links to print Bibles,
				print-on-demand Bibles, digital Bibles, audio Bibles, visual Bibles and
				Bible resources – in multiple formats.
			</p>
			<p>
				Find-A-Bible is a collaborative project by the Forum of Bible Agencies
				International built to increase the Bible's accessibility and to
				facilitate access to scripture. The Find-A-Bible design, interphase, and
				database was created and is maintained by the Digital Bible Society
				(dbs.org).
			</p>
		</div>
