					target="_blank">
					SIL 639-3 Language Codes
				</a>
				and the
				<a
					href="https://www.iso.org/iso-3166-country-codes.html"
					target="_blank">
					ISO-3166 International Standard Country Codes
				</a>
				.
			</p>
			<p>
				<b>Bible Resource Data</b>
