			<h4 class="medium-mb2">Data Sources</h4>
			<p>
				Find-A-Bible exists to provide tools that simplify the discovery of
				existing Bible resources. Find-A-Bible also exists to inform missions
				workers of what languages they may likely encounter in given regions and
				then to equip them with resources that will serve their mission. Toward
				those goals, Find-A-Bible draws upon data derived from a wide variety of
				reliable open sources, and in some case uses calculates averages and
				best estimates for determining numbers.
			</p>
			<p>
				Consequently, <b>
					Find-A-Bible should NOT be considered a definitive source for global
					and linguistic data and statistics
				</b>
				. For reliable and authoritative data, we strongly recommend you refer to
				the latest version of the
				<a href="https://www.ethnologue.com/books" target="_blank">
					SIL Ethnologue
				</a>
				(or subscribe to
				<a href="https://www.ethnologue.com/" target="_blank">Ethnologue.com</a>
				), or member agencies of the
				<a href="https://unitedbiblesocieties.org/" target="_blank">
					United Bible Societies
				</a>
				.
			</p>
		</div>
	</div>

	<div class="flex justify-between">
